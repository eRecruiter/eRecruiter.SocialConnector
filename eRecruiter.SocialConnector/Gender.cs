namespace eRecruiter.SocialConnector
{
    /// <summary>
    /// Provides values representing a person's gender. 
    /// </summary>
    public enum Gender
    {
        /// <summary>
        /// The person is male. 
        /// </summary>
        Male,

        /// <summary>
        /// The person is female. 
        /// </summary>
        Female,

        /// <summary>
        /// The gender is unknown or has a value that is not understood by this client. 
        /// </summary>
        NotSet
    }
}